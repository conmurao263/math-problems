<?php
// Example PHP code for math-problems

// Function to add two numbers
function sumNumbers($a, $b) {
    return $a + $b;
}

// Call the function and print the result
$result = sumNumbers(5, 3);
echo "The sum of 5 and 3 is: " . $result;

